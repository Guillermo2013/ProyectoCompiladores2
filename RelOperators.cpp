

void Relacionales() {
    int x2, y2;
     
    x2 = 100 / 22;
    y2 = 100 / 52;

    printf("x = %d\n", x2);
    printf("y = %d\n", y2);

    // Greather than
    printf ("(x > y: %d) ", x2 > y2);

    // Less than
    printf("(x < y: %d )", x2 < y2);

    // Greather or Equal
    printf("(x >= y: %d) ", x2 >= y2);

    // Less or Equal
    printf("(x <= y: %d )", x2 <= y2);

    // Not Equal
    printf("(x != y: %d) ", x2 != y2);

    // Equal
    printf("(x == y: %d)\n ", x2 == y2);

}
